 class Q5 {
  public static void main(String[] args) {
    String s ="Hello World";

    //Write a Java program using given string operations: equals(), Substring(), contains(), concat(), replace(), indexOf(), toLowerCase(), toUpperCase()
    System.out.println("String s = " + s);
    System.out.println("s.equals(\"Hello World\") = " + s.equals("Hello World"));
    System.out.println("s.substring(0, 5) = " + s.substring(0, 5));
    System.out.println("s.contains(\"World\") = " + s.contains("World"));
    System.out.println("s.concat(\"!\") = " + s.concat("!"));
    System.out.println("s.replace(\"World\", \"Universe\") = " + s.replace("World", "Universe"));
    System.out.println("s.indexOf(\"World\") = " + s.indexOf("World"));
    System.out.println("s.toLowerCase() = " + s.toLowerCase());
    System.out.println("s.toUpperCase() = " + s.toUpperCase());




  }  
}
