class Q13 {
    public static void main(String[] args) {
        int i = 7;

        System.out.println("System.out.println(++i)  " + ++i + " pre-increment");
        System.out.println("System.out.println(i)  " + i);
        System.out.println("System.out.println(i++) " + i++ + " post-increment");
        System.out.println("System.out.println(i)  " + i);
        System.out.println("System.out.println(--i) " + --i + " pre-decrement");
        System.out.println("System.out.println(i)  " + i);
        System.out.println("System.out.println(i--) " + i-- + " post-decrement");
        System.out.println("System.out.println(i)  " + i);
    }
}
